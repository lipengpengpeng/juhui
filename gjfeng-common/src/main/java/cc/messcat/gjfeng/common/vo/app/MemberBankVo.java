package cc.messcat.gjfeng.common.vo.app;

import java.io.Serializable;

public class MemberBankVo implements Serializable {

	private Long id;
	private String bankName;
	private String bankPic;
	private String bankCode;
	private String bankSub;
	private String bankCard;
	private String bankColor;
	private String holder;
	
	public String getBankSub() {
		return bankSub;
	}
	public void setBankSub(String bankSub) {
		this.bankSub = bankSub;
	}
	public String getBankCard() {
		return bankCard;
	}
	public void setBankCard(String bankCard) {
		this.bankCard = bankCard;
	}
	public String getHolder() {
		return holder;
	}
	public void setHolder(String holder) {
		this.holder = holder;
	}
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	
	public String getBankName() {
		return bankName;
	}
	public void setBankName(String bankName) {
		this.bankName = bankName;
	}
	
	public String getBankPic() {
		return bankPic;
	}
	public void setBankPic(String bankPic) {
		this.bankPic = bankPic;
	}
	public String getBankCode() {
		return bankCode;
	}
	public void setBankCode(String bankCode) {
		this.bankCode = bankCode;
	}
	public String getBankColor() {
		return bankColor;
	}
	public void setBankColor(String bankColor) {
		this.bankColor = bankColor;
	}
	
	
}
